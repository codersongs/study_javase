package com.codersongs.javase.datatype;

/**
 * 基本数据类型
 */
public class BasicType {
    public static void main(String[] args) {
        Integer a = 1;
        Integer b = 1;
        System.out.println(a == b);

        Float c = 1.0F;
        Float d = 1.0F;
        System.out.println(c == d);
    }
}
